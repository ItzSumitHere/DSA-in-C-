// Any year in input through the keyboard . write a program to determine whether the year is a leap year or not.

#include <iostream>
using namespace std;

int main(){

    int a;
    cout<<"Enter your year....!";
    cin>>a;
    if (a%4==0){cout<<a<<" is the leap year";}
    else{cout<<a<<" is not a leap year";}


}